function emitNormalPage(
  item: RouteItem,
  fieldsRaw: ContentField[],
): { code: string; counts: ManifestEntry['componentCounts']; warnings: string[] } {
  const componentName = pascalCase(item.key) + 'Page';
  const warnings: string[] = [];
  const counts = { EditableContent: 0, EditableRichText: 0, EditableImage: 0 };

  // Drop fields that should never render in body (admin still edits them, head still reads them).
  const afterSkip = fieldsRaw.filter((f) => !BODY_SKIP_KEYS.has(f.field_key));

  // Collapse same-value duplicates within this page.
  const fields = dedupeByValue(item.key, afterSkip);

  // Group fields
  const groups = new Map<string, ContentField[]>();
  for (const f of fields) {
    const g = groupKey(f.field_key);
    if (!groups.has(g)) groups.set(g, []);
    groups.get(g)!.push(f);
  }

  // Build a set of image url keys for which we already emit an EditableImage,
  // so we can skip the matching .alt key when iterating.
  const imageUrlKeys = new Set<string>();
  const skipKeysInBody = new Set<string>();
  for (const f of fields) {
    if (f.value_type === 'url' && /^image\.\d+\.url$/.test(f.field_key)) {
      imageUrlKeys.add(f.field_key);
      const altKey = f.field_key.replace(/\.url$/, '.alt');
      skipKeysInBody.add(altKey);
    }
  }

  // Imports (set conditionally)
  let usesEditableContent = false;
  let usesEditableRichText = false;
  let usesEditableImage = false;

  // Decide if we need fetchAssets/AssetProvider
  const hasImageKeys = (groups.get('image') ?? []).some((f) =>
    /^image\.\d+\.(url|alt)$/.test(f.field_key) || /^image\.\d+$/.test(f.field_key),
  );

  // Build sections in order
  const sectionLines: string[] = [];
  for (const g of GROUP_ORDER) {
    const fs = groups.get(g);
    if (!fs || fs.length === 0) continue;
    if (g === 'meta') continue; // head only

    const lines: string[] = [];
    if (g === 'image') {
      // Render one EditableImage per image.NN group (using the url key).
      const seen = new Set<string>();
      for (const f of fs) {
        const m = /^image\.(\d+)\.(url|alt)$/.exec(f.field_key) ?? /^image\.(\d+)$/.exec(f.field_key);
        if (!m) {
          // Unrecognized image key shape — emit as text fallthrough.
          lines.push(`        <EditableContent fieldKey="${f.field_key}" as="${asTagFor(f.field_key)}" />`);
          usesEditableContent = true;
          counts.EditableContent++;
          continue;
        }
        const idx = m[1];
        if (seen.has(idx)) continue;
        seen.add(idx);
        // Prefer the .url-suffixed key when present, else the bare image.NN key.
        const urlKey = imageUrlKeys.has(`image.${idx}.url`) ? `image.${idx}.url` : `image.${idx}`;
        lines.push(`        <EditableImage fieldKey="${urlKey}" />`);
        usesEditableImage = true;
        counts.EditableImage++;
      }
    } else {
      for (const f of fs) {
        if (skipKeysInBody.has(f.field_key)) continue;
        if (f.field_key.startsWith('seo.')) continue;
        if (f.value_type === 'meta') continue;

        const tag = asTagFor(f.field_key);

        if (f.value_type === 'html' || f.value_type === 'richtext' || f.value_type === 'markdown') {
          lines.push(`        <EditableRichText fieldKey="${f.field_key}" />`);
          usesEditableRichText = true;
          counts.EditableRichText++;
        } else if (f.value_type === 'datetime' || f.value_type === 'date') {
          lines.push(`        <EditableContent fieldKey="${f.field_key}" as="time" />`);
          usesEditableContent = true;
          counts.EditableContent++;
        } else if (f.value_type === 'url' && f.field_key.startsWith('image.')) {
          // Stray image url that didn't match shape — render as text link target.
          lines.push(`        <EditableContent fieldKey="${f.field_key}" as="span" />`);
          usesEditableContent = true;
          counts.EditableContent++;
        } else {
          lines.push(`        <EditableContent fieldKey="${f.field_key}" as="${tag}" />`);
          usesEditableContent = true;
          counts.EditableContent++;
        }
      }
    }

    if (lines.length === 0) continue;
    sectionLines.push(`      <section data-group="${g}">`);
    sectionLines.push(...lines);
    sectionLines.push(`      </section>`);
  }

  // Index types get a list-children placeholder
  if (item.type === 'index') {
    sectionLines.push(`      <section data-group="list-placeholder">`);
    sectionLines.push(`        {/* TODO Phase H: <ListSection /> for child query */}`);
    sectionLines.push(`      </section>`);
  }

  // Warnings
  const bodyFieldCount = fields.filter(
    (f) => !f.field_key.startsWith('meta.') && !f.field_key.startsWith('seo.'),
  ).length;
  if (bodyFieldCount === 0 && item.type !== 'index_filter') {
    warnings.push('zero body field_keys (only meta.*)');
  }
  if (!hasImageKeys && ['team', 'sermon', 'blog'].includes(item.type)) {
    warnings.push(`no image.* keys but type=${item.type} (probably needs hero image)`);
  }

  // Imports
  const imports: string[] = [
    '// GENERATED by scripts/codegen-pages.ts — do not hand-edit',
    'import type { Metadata } from "next";',
  ];
  if (hasImageKeys) {
    imports.push('import { fetchPageContent, fetchAssets } from "@/lib/content";');
  } else {
    imports.push('import { fetchPageContent } from "@/lib/content";');
  }
  imports.push('import { ContentProviderWrapper } from "@/components/ContentProviderWrapper";');
  if (hasImageKeys) {
    imports.push('import { AssetProviderWrapper } from "@/components/AssetProviderWrapper";');
  }
  if (usesEditableContent) imports.push('import { EditableContent } from "@/components/EditableContent";');
  if (usesEditableRichText) imports.push('import { EditableRichText } from "@/components/EditableRichText";');
  if (usesEditableImage) imports.push('import { EditableImage } from "@/components/EditableImage";');

  // Default body fallback if no sections were emitted
  if (sectionLines.length === 0) {
    sectionLines.push(`      <section data-group="empty">`);
    sectionLines.push(`        {/* No body field_keys for this page yet. Add rows in admin to populate. */}`);
    sectionLines.push(`      </section>`);
  }

  const dataFetch = hasImageKeys
    ? `  const [content, assets] = await Promise.all([
    fetchPageContent(PAGE_KEY),
    fetchAssets(PAGE_KEY + "."),
  ]);
  const contentEntries = Array.from(content.values());
  const assetEntries = Array.from(assets.values());`
    : `  const content = await fetchPageContent(PAGE_KEY);
  const contentEntries = Array.from(content.values());`;

  const wrapOpen = hasImageKeys
    ? `    <ContentProviderWrapper pageKey={PAGE_KEY} entries={contentEntries}>
      <AssetProviderWrapper entries={assetEntries}>`
    : `    <ContentProviderWrapper pageKey={PAGE_KEY} entries={contentEntries}>`;

  const wrapClose = hasImageKeys
    ? `      </AssetProviderWrapper>
    </ContentProviderWrapper>`
    : `    </ContentProviderWrapper>`;

  // Indent each section line one extra level if AssetProviderWrapper is nested
  const renderedSections = hasImageKeys
    ? sectionLines.map((l) => '  ' + l).join('\n')
    : sectionLines.join('\n');

  const code = `${imports.join('\n')}

const PAGE_KEY = "${item.key}";

export async function generateMetadata(): Promise<Metadata> {
  const [page, global] = await Promise.all([
    fetchPageContent(PAGE_KEY),
    fetchPageContent("global"),
  ]);
  const title = page.get("meta.title")?.value ?? global.get("meta.title")?.value ?? "";
  const description = page.get("meta.description")?.value ?? global.get("meta.description")?.value ?? "";
  return { title, description };
}

export default async function ${componentName}() {
${dataFetch}
  return (
${wrapOpen}
${renderedSections}
${wrapClose}
  );
}
`;

  return { code, counts, warnings };
}

// ── Main ──

async function main() {
  console.log('codegen-pages: starting (preview only — writes to site/.codegen-preview/)\n');

  const routeMap = JSON.parse(await readFile(ROUTE_MAP_PATH, 'utf8')) as RouteMap;
  console.log(`Loaded route-map: ${routeMap.count} items\n`);

  const sb = getSupabase();

  await mkdir(OUT_DIR, { recursive: true });

  const manifest: ManifestEntry[] = [];
  const outFileToKeys = new Map<string, string[]>();
  const skippedHandEdited: string[] = [];

  for (const item of routeMap.items) {
    const outRel = outputPathFor(item);
    const outAbs = join(OUT_DIR, outRel);

    // Track collisions
    if (!outFileToKeys.has(outRel)) outFileToKeys.set(outRel, []);
    outFileToKeys.get(outRel)!.push(item.key);

    let code: string;
    let counts: ManifestEntry['componentCounts'];
    let warnings: string[] = [];
    let fieldKeyCount = 0;

    const redirectTo = pickRedirect(item);
    const tpl = pickTemplate(item);

    if (redirectTo) {
      // Redirect shim wins over template/per-key dump. Used for de-duplicated
      // routes (e.g., team.bishopwilson → /team/bishop-huel-wilson).
      const r = emitRedirectShim(redirectTo);
      code = r.code;
      counts = r.counts;
      fieldKeyCount = 0;
    } else if (tpl) {
      // Registered template wins over per-key dump.
      const r = emitTemplateShim(item, tpl);
      code = r.code;
      counts = r.counts;
      // Field count is irrelevant for template shims — the template owns rendering.
      fieldKeyCount = 0;
    } else if (item.type === 'index_filter') {
      // Don't query Supabase for filters — they reuse parent index content.
      const r = emitIndexFilterStub(item);
      code = r.code;
      counts = r.counts;
      fieldKeyCount = 0;
    } else {
      const fields = await fetchFields(sb, item.key);
      fieldKeyCount = fields.length;
      const r = emitNormalPage(item, fields);
      code = r.code;
      counts = r.counts;
      warnings = r.warnings;
    }

    // Safety: if a file already exists at the target and does NOT start with
    // the GENERATED marker, treat it as hand-edited and skip the write.
    let exists = false;
    try {
      await stat(outAbs);
      exists = true;
    } catch {
      exists = false;
    }
    if (exists) {
      const existing = await readFile(outAbs, 'utf8');
      if (!existing.startsWith(GENERATED_MARKER)) {
        skippedHandEdited.push(outRel);
        warnings.push('skipped: existing file is hand-edited (no GENERATED marker)');
        manifest.push({
          key: item.key,
          type: item.type,
          newPath: item.newPath,
          outFile: outRel,
          fieldKeyCount,
          componentCounts: counts,
          warnings,
        });
        continue;
      }
    }

    await mkdir(dirname(outAbs), { recursive: true });
    await writeFile(outAbs, code, 'utf8');

    manifest.push({
      key: item.key,
      type: item.type,
      newPath: item.newPath,
      outFile: outRel,
      fieldKeyCount,
      componentCounts: counts,
      warnings,
    });

    console.log(`  ✓ ${item.key.padEnd(50)} → ${outRel}  (${fieldKeyCount} fields, ${counts.EditableContent}c/${counts.EditableRichText}r/${counts.EditableImage}i)`);
  }

  // Resolve collision warnings post-hoc
  for (const [outFile, keys] of outFileToKeys) {
    if (keys.length > 1) {
      for (const entry of manifest) {
        if (entry.outFile === outFile) {
          entry.warnings.push(`PATH COLLISION with: ${keys.filter((k) => k !== entry.key).join(', ')}`);
        }
      }
    }
  }

  // Aggregate component counts for summary
  const totals = manifest.reduce(
    (acc, e) => {
      acc.EditableContent += e.componentCounts.EditableContent;
      acc.EditableRichText += e.componentCounts.EditableRichText;
      acc.EditableImage += e.componentCounts.EditableImage;
      return acc;
    },
    { EditableContent: 0, EditableRichText: 0, EditableImage: 0 },
  );

  const allWarnings = manifest.flatMap((e) => e.warnings.map((w) => `${e.key}: ${w}`));

  const manifestDoc = {
    generatedAt: new Date().toISOString(),
    count: manifest.length,
    componentTotals: totals,
    files: manifest,
  };
  await writeFile(MANIFEST_PATH, JSON.stringify(manifestDoc, null, 2) + '\n', 'utf8');

  console.log(`\n═══ MANIFEST SUMMARY ═══\n`);
  console.log(`Total files generated: ${manifest.length}`);
  console.log(`Component totals: EditableContent=${totals.EditableContent}, EditableRichText=${totals.EditableRichText}, EditableImage=${totals.EditableImage}`);
  console.log(`Warnings: ${allWarnings.length}`);
  for (const w of allWarnings) console.log(`  ⚠ ${w}`);

  // Per-page dedupe summary — top 10 pages by collapsed count
  if (dedupeReport.length > 0) {
    dedupeReport.sort((a, b) => b.collapsed - a.collapsed);
    const totalCollapsed = dedupeReport.reduce((n, r) => n + r.collapsed, 0);
    console.log(`\n═══ DEDUPE REPORT ═══`);
    console.log(`Pages with collapsed rows: ${dedupeReport.length} (total rows collapsed: ${totalCollapsed})`);
    const top = dedupeReport.slice(0, 10);
    for (const r of top) {
      console.log(`\n  ${r.pageKey}  (collapsed ${r.collapsed})`);
      for (const d of r.details) {
        console.log(`    KEPT  ${d.kept}  ← "${d.sample}"`);
        for (const lost of d.dropped) console.log(`    LOST  ${lost}`);
      }
    }
    if (dedupeReport.length > 10) {
      console.log(`\n  … and ${dedupeReport.length - 10} more page(s) with collapsed rows`);
    }
  } else {
    console.log(`\nDedupe report: no same-value duplicates found`);
  }

  console.log(`\nManifest written to: ${MANIFEST_PATH}`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
