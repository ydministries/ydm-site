#!/usr/bin/env npx tsx
/**
 * codegen-pages.ts — Phase F preview generator.
 *
 * Pure function of:
 *   - archive/wordpress/scrape/route-map.json   (route source + new paths + types)
 *   - Supabase page_content table               (actual seeded field_keys per page)
 *
 * Writes generated page.tsx files to site/.codegen-preview/ — DOES NOT touch
 * site/src/app/(site)/. Also writes site/.codegen-preview/MANIFEST.json.
 *
 * Run:  npm run codegen:pages
 */

import { readFile, writeFile, mkdir, rm, stat } from 'node:fs/promises';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { config as loadEnv } from 'dotenv';
import { createClient, type SupabaseClient } from '@supabase/supabase-js';
import { templateRegistry, type TemplateRef } from '../src/components/templates/registry';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const SITE_DIR = resolve(__dirname, '..');
const REPO_DIR = resolve(SITE_DIR, '..');

loadEnv({ path: join(SITE_DIR, '.env.local') });

const ROUTE_MAP_PATH = join(REPO_DIR, 'archive/wordpress/scrape/route-map.json');
// Codegen writes directly into the live (site) tree. Files that don't start
// with the GENERATED marker are preserved (hand-edited surfaces).
const OUT_DIR = join(SITE_DIR, 'src/app/(site)');
const MANIFEST_PATH = join(SITE_DIR, '.codegen-manifest.json');
const GENERATED_MARKER = '// GENERATED by scripts/codegen-pages.ts';

// ── Types ──

type RouteItem = {
  oldPath: string;
  oldSlug: string;
  newPath: string;
  type: string;
  key: string;
  scrapeFile: string;
  aliases: string[];
};

type RouteMap = { generatedAt: string; count: number; items: RouteItem[] };

type ContentField = { field_key: string; value_type: string; value: string };

// Fields that exist in the DB (so admin can edit, generateMetadata can read)
// but should NEVER render as visible elements in the page body.
const BODY_SKIP_KEYS = new Set<string>([
  'slug',
  'date_published',
  'excerpt',
  'body_html',           // full HTML blob — duplicates h1/h2/p decomposition
  'meta.title',          // lives in <head> via generateMetadata
  'meta.description',    // <head>
  'meta.og_image',       // <head>
]);

// Mechanical (decomposed) prefixes — heading hierarchy that should be preferred
// over a heading key losing to a non-mechanical equivalent only when no semantic
// option is available.
const MECHANICAL_PREFIXES = new Set<string>([
  'h1', 'h2', 'h3', 'h4', 'h5', 'h6',
  'p', 'list', 'quote', 'block', 'image', 'form',
]);

function priorityRank(fieldKey: string): number {
  const prefix = fieldKey.split('.')[0];
  // 1 = semantic (dotted, non-mechanical prefix)  ← e.g. "pray.title"
  if (fieldKey.includes('.') && !MECHANICAL_PREFIXES.has(prefix)) return 1;
  // 2 = heading mechanical
  if (/^h[1-6]\./.test(fieldKey)) return 2;
  // 3 = anything else
  return 3;
}

type ManifestEntry = {
  key: string;
  type: string;
  newPath: string;
  outFile: string;
  fieldKeyCount: number;
  componentCounts: {
    EditableContent: number;
    EditableRichText: number;
    EditableImage: number;
  };
  warnings: string[];
};

// ── Helpers ──

function pascalCase(s: string): string {
  return s
    .replace(/[^a-zA-Z0-9]+/g, ' ')
    .split(' ')
    .filter(Boolean)
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join('');
}

function outputPathFor(item: RouteItem): string {
  // Path is RELATIVE to OUT_DIR (which is src/app/(site)). The (site) route
  // group prefix is supplied by OUT_DIR — do NOT prepend it here, otherwise
  // files land at src/app/(site)/(site)/...
  if (item.type === 'index_filter') {
    // Filter routes share the index's base path with a query string in newPath.
    // Each filter becomes a unique synthetic route under (site) using its
    // oldPath. Phase H will collapse these into ?query= params on the parent.
    const segs = item.oldPath.split('/').filter(Boolean);
    return join(...segs, 'page.tsx');
  }
  const path = item.newPath.split('?')[0];
  if (path === '/' || path === '') return 'page.tsx';
  const segs = path.split('/').filter(Boolean);
  return join(...segs, 'page.tsx');
}

function asTagFor(fieldKey: string): string {
  if (fieldKey.startsWith('h1.')) return 'h1';
  if (fieldKey.startsWith('h2.')) return 'h2';
  if (fieldKey.startsWith('h3.')) return 'h3';
  if (fieldKey.startsWith('h4.')) return 'h4';
  if (fieldKey.startsWith('h5.')) return 'h5';
  if (fieldKey.startsWith('h6.')) return 'h6';
  if (fieldKey.startsWith('meta.')) return 'span';
  if (fieldKey.startsWith('form.label.')) return 'label';
  if (fieldKey === 'form.button' || fieldKey.startsWith('form.button.')) return 'span';
  return 'span';
}

function groupKey(fieldKey: string): string {
  if (/^h[1-6]\./.test(fieldKey)) return 'h' + fieldKey.charAt(1);
  if (fieldKey.startsWith('p.')) return 'p';
  if (fieldKey.startsWith('list.')) return 'list';
  if (fieldKey.startsWith('quote.')) return 'quote';
  if (fieldKey.startsWith('block.')) return 'block';
  if (fieldKey.startsWith('image.')) return 'image';
  if (fieldKey.startsWith('form.')) return 'form';
  if (fieldKey.startsWith('meta.')) return 'meta';
  return 'other';
}

// Section render order
const GROUP_ORDER = ['h1', 'h2', 'h3', 'h4', 'h5', 'h6', 'p', 'list', 'quote', 'block', 'image', 'form', 'other'];

// ── Supabase ──

function getSupabase(): SupabaseClient {
  const url = process.env.NEXT_PUBLIC_SUPABASE_URL ?? process.env.SUPABASE_URL;
  const key = process.env.SUPABASE_SERVICE_ROLE_KEY;
  if (!url || !key) {
    console.error('Missing NEXT_PUBLIC_SUPABASE_URL/SUPABASE_URL or SUPABASE_SERVICE_ROLE_KEY in site/.env.local');
    process.exit(1);
  }
  return createClient(url, key, { auth: { autoRefreshToken: false, persistSession: false } });
}

async function fetchFields(sb: SupabaseClient, pageKey: string): Promise<ContentField[]> {
  const { data, error } = await sb
    .from('page_content')
    .select('field_key, value_type, value')
    .eq('page_key', pageKey)
    .order('field_key', { ascending: true });
  if (error) {
    console.error(`[supabase] fetch fields for "${pageKey}":`, error.message);
    return [];
  }
  return (data ?? []) as ContentField[];
}

type DedupeReportEntry = { pageKey: string; collapsed: number; details: Array<{ kept: string; dropped: string[]; sample: string }> };
const dedupeReport: DedupeReportEntry[] = [];

/**
 * Lower-case + collapse smart-quotes, em/en-dashes, and whitespace so that
 * cosmetic punctuation differences don't prevent deduplication. The displayed
 * value remains the original — this is comparison-only.
 */
function normalizeForDedupe(v: string): string {
  return v
    .toLowerCase()
    .replace(/[‘’]/g, "'")     // smart single quotes → '
    .replace(/[“”]/g, '"')     // smart double quotes → "
    .replace(/[–—]/g, '-')     // en-dash, em-dash → -
    .replace(/\s+/g, ' ')                 // collapse all whitespace
    .trim();
}

/**
 * Collapse fields that share the same value (under normalized comparison).
 * Returns the surviving subset (preserving the original sort order) plus
 * the per-page collapse details.
 *
 * Priority: lower priorityRank wins. Tie-breaker: original (alphabetical) order.
 */
function dedupeByValue(pageKey: string, fields: ContentField[]): ContentField[] {
  const orderIndex = new Map<string, number>();
  fields.forEach((f, i) => orderIndex.set(f.field_key, i));

  // Group keys by NORMALIZED value (cosmetic punctuation/whitespace ignored).
  const byNormalized = new Map<string, ContentField[]>();
  for (const f of fields) {
    const orig = (f.value ?? '').trim();
    if (!orig) continue;
    const norm = normalizeForDedupe(orig);
    if (!byNormalized.has(norm)) byNormalized.set(norm, []);
    byNormalized.get(norm)!.push(f);
  }

  const winners = new Set<string>(); // field_keys to keep
  const losersByWinner = new Map<string, string[]>();
  const winnerSample = new Map<string, string>();

  for (const f of fields) {
    if ((f.value ?? '').trim() === '') {
      winners.add(f.field_key); // empties are never collapsed
    }
  }

  for (const [, group] of byNormalized) {
    if (group.length === 1) {
      winners.add(group[0].field_key);
      continue;
    }
    // Sort by priority then by original order
    const sorted = [...group].sort((a, b) => {
      const pa = priorityRank(a.field_key);
      const pb = priorityRank(b.field_key);
      if (pa !== pb) return pa - pb;
      return (orderIndex.get(a.field_key) ?? 0) - (orderIndex.get(b.field_key) ?? 0);
    });
    const kept = sorted[0];
    winners.add(kept.field_key);
    const dropped = sorted.slice(1).map((f) => f.field_key);
    losersByWinner.set(kept.field_key, dropped);
    // Sample uses the *original* (non-normalized) value of the surviving row.
    const display = (kept.value ?? '').trim();
    winnerSample.set(kept.field_key, display.length > 80 ? display.slice(0, 77) + '…' : display);
  }

  if (losersByWinner.size > 0) {
    const collapsed = [...losersByWinner.values()].reduce((n, ds) => n + ds.length, 0);
    dedupeReport.push({
      pageKey,
      collapsed,
      details: [...losersByWinner.entries()].map(([kept, dropped]) => ({
        kept,
        dropped,
        sample: winnerSample.get(kept) ?? '',
      })),
    });
  }

  return fields.filter((f) => winners.has(f.field_key));
}

// ── Page emit ──

function pickTemplate(item: RouteItem): TemplateRef | null {
  return (
    templateRegistry.byKey[item.key] ??
    templateRegistry.byType[item.type] ??
    templateRegistry.fallback ??
    null
  );
}

function pickRedirect(item: RouteItem): string | null {
  return templateRegistry.redirectKeys?.[item.key] ?? null;
}

function emitRedirectShim(destUrl: string): { code: string; counts: ManifestEntry['componentCounts'] } {
  const counts = { EditableContent: 0, EditableRichText: 0, EditableImage: 0 };
  const code = `// GENERATED by scripts/codegen-pages.ts — do not hand-edit
import { redirect } from "next/navigation";

export default function Page() {
  redirect(${JSON.stringify(destUrl)});
}
`;
  return { code, counts };
}

function emitTemplateShim(item: RouteItem, tpl: TemplateRef): { code: string; counts: ManifestEntry['componentCounts'] } {
  const componentName = pascalCase(item.key) + 'Page';
  // Shim wires both providers so the template can use Editable* freely. We
  // count the template render itself as a single EditableContent slot for the
  // manifest's component totals — accurate per-component counts live in the
  // template file itself.
  const counts = { EditableContent: 1, EditableRichText: 0, EditableImage: 0 };
  const code = `// GENERATED by scripts/codegen-pages.ts — do not hand-edit
import type { Metadata } from "next";
import { fetchPageContent, fetchAssets } from "@/lib/content";
import { ContentProviderWrapper } from "@/components/ContentProviderWrapper";
import { AssetProviderWrapper } from "@/components/AssetProviderWrapper";
import { ${tpl.exportName} } from "${tpl.importPath}";

const PAGE_KEY = "${item.key}";

export async function generateMetadata(): Promise<Metadata> {
  const [page, global] = await Promise.all([
    fetchPageContent(PAGE_KEY),
    fetchPageContent("global"),
  ]);
  const title = page.get("meta.title")?.value ?? global.get("meta.title")?.value ?? "";
  const description = page.get("meta.description")?.value ?? global.get("meta.description")?.value ?? "";
  return { title, description };
}

export default async function ${componentName}() {
  const [content, assets] = await Promise.all([
    fetchPageContent(PAGE_KEY),
    fetchAssets(PAGE_KEY + "."),
  ]);
  const contentEntries = Array.from(content.values());
  const assetEntries = Array.from(assets.values());
  return (
    <ContentProviderWrapper pageKey={PAGE_KEY} entries={contentEntries}>
      <AssetProviderWrapper entries={assetEntries}>
        <${tpl.exportName} pageKey={PAGE_KEY} />
      </AssetProviderWrapper>
    </ContentProviderWrapper>
  );
}
`;
  return { code, counts };
}

